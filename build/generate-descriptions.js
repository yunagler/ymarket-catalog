// Default template for unknown categories
const DEFAULT_TEMPLATES = [
  (n, u) => `${n} - מוצר מקצועי למוסדות ועסקים.${u ? ` באריזת ${u}.` : ''} מחיר סיטונאי עם משלוח ארצי מוואי מרקט.`,
  (n, u) => `${n} - פתרון מקצועי לעסקים ומוסדות.${u ? ` ${u} באריזה מוסדית.` : ''} איכות מעולה במחיר סיטונאי. משלוח ארצי מוואי מרקט.`,
  (n, u) => `${n} - מוצר איכותי לשימוש מוסדי ומסחרי.${u ? ` באריזת ${u}.` : ''} מחיר סיטונאי. הזמנה ומשלוח ארצי מוואי מרקט.`,
];

function generateDescription(item) {
  const name = item.name.trim();
  const category = item.categoryName || '';
  const unit = (item.unit || '').trim();

  const templates = TEMPLATES[category] || DEFAULT_TEMPLATES;

  // Use a deterministic index based on item id for consistent results
  const variantIndex = item.id % templates.length;

  return templates[variantIndex](name, unit);
}

function main() {
  console.log('Reading products.json...');
  const data = JSON.parse(fs.readFileSync(DATA_PATH, 'utf8'));

  console.log(`Total items: ${data.items.length}`);

  let generated = 0;
  let skipped = 0;
  const categoryCounts = {};

  for (const item of data.items) {
    // Only update empty/missing descriptions
    if (item.description && item.description.trim() !== '') {
      skipped++;
      continue;
    }

    item.description = generateDescription(item);
    generated++;

    const cat = item.categoryName || 'unknown';
    categoryCounts[cat] = (categoryCounts[cat] || 0) + 1;
  }

  console.log(`\nResults:`);
  console.log(`  Generated: ${generated} descriptions`);
  console.log(`  Skipped (existing): ${skipped} items`);
  console.log(`\nBy category:`);
  for (const [cat, count] of Object.entries(categoryCounts).sort((a, b) => b[1] - a[1])) {
    console.log(`  ${cat}: ${count}`);
  }

  // Write back
  fs.writeFileSync(DATA_PATH, JSON.stringify(data, null, 2), 'utf8');
  console.log(`\nUpdated products.json saved successfully.`);
}

main();
